import { GoogleGenAI } from "@google/genai";

export async function generateRealisticHelmet() {
  const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY });
  const response = await ai.models.generateContent({
    model: 'gemini-2.5-flash-image',
    contents: {
      parts: [
        {
          text: 'A professional 3D icon of a yellow construction safety helmet, side view, realistic textures, glossy finish, soft shadows, isolated on a white background, high quality 3D render, professional UI asset.',
        },
      ],
    },
    config: {
      imageConfig: {
        aspectRatio: "1:1"
      }
    }
  });

  for (const part of response.candidates[0].content.parts) {
    if (part.inlineData) {
      return `data:image/png;base64,${part.inlineData.data}`;
    }
  }
  return null;
}
